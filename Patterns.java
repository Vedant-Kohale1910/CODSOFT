package com.java;

public class Patterns{
    public static void triangle(int n){
        int i,j;
        for(i=0; i<=0; i++){
            for(j=0; j<=i; j++){
                System.out.print("");
            }
            System.out.println("*");
        }
    }
    public static void main(String[] args){
        int n = 4;
        triangle(n);
    }
}
